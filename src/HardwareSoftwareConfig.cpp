// Calculate timedelta matrix and store as constant in flash memory
// Formula: dt(n) = 60*[(Pw)/(r0 - dw*n)]*[1/(2*PI)]
constexpr float formula_rn(int n){
    return ((pixel_height/(fan_radius - n*pixel_spacing))/(2*PI));
}

constexpr auto makeArray_dt_half() {
    std::array<float, NUMPIXELS / 2> arr{};   // use std::array
    for (int i = 0; i < NUMPIXELS / 2; i++) {
        arr[i] = formula_rn(i);
    }
    return arr;
}

// Expand to 36 elements, mirrored with same edge value
constexpr auto makeArray_dt_full() {
    constexpr auto half = makeArray_dt_half();
    std::array<float, NUMPIXELS> arr{};

    for (int i = 0; i < NUMPIXELS / 2; i++) {
        arr[i] = half[i];                                 // left half
        arr[NUMPIXELS - 1 - i] = half[i];                 // mirrored right half
    }
    return arr;
}

